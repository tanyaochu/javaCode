package Tanyc.Grammar;

public class DifferentKindOfVariable {
    public static void main(String[] args) {

        //整数类型
        byte nmu1 = 10;
        int nmu2 = 20;
        short nmu3 = 30;
        long nmu4 = 123456789123456789L;
        System.out.println(nmu1);
        System.out.println(nmu2);
        System.out.println(nmu3);
        System.out.println(nmu4);
        System.out.println("-------------------------");

        //小数类型
        float nmu5 = 17.0F;
        double nmu6 = 18.6;
        System.out.println(nmu5);
        System.out.println(nmu6);
        System.out.println("-------------------------");

        //字符变量
        char c1 = '中';
        char c2 = 'a';
        char c3 = 97;
        System.out.println(c1);
        System.out.println(c2);
        System.out.println(c3);
        System.out.println("-------------------------");

        //布尔变量
        boolean s1 = false;
        boolean s2 = true;
        System.out.println(s1);
        System.out.println(s2);
        System.out.println("-------------------------");
        //案例
        String x1 = "谭耀楚";
        int x2 = 19;
        char x3 = '男';
        double x4 = 173.5;
        boolean x5 = false;
        System.out.println(x1);
        System.out.println(x2);
        System.out.println(x3);
        System.out.println(x4);
        System.out.println(x5);
    }

}
